neighborsXY(C0,C1):-
    C1 is C0 -1; %down and left
    C1 is C0 +1. %up and right

%left right
neighbors(pos(X0,Y0), pos(X1,Y1)):-
    neighborsXY(X0,X1),
    Y0 = Y1.

%up down
neighbors(pos(X0,Y0),pos(X1,Y1)):-
    X0 = X1,    
    neighborsXY(Y0,Y1).

color(Same,pos(X,Y),Color) :-
    nth0(Y,Same,Column),
    nth0(X,Column,Color).

same_color_neighbors(Same,P,N) :-
    neighbors(P,N),
    color(Same,P,C),
